# this program illustrates how to do calculations in python
# Add
a = 2 + 2
print(a)
# Sub
b = a - 1
print(b)
# Multiply
c = a * b
print(c)
# Divide
d = c/a # classic division (/) returns a float
print(d)
e = 17 / 3 # floor division (//) discards the fractional part
print(e)
f = 17 % 3 # the % operator returns the remainder of the division
print(f)
#Square and Power of
g = 5 ** 2 #5^2
print(g)
h = 2 ** 7 #2 to the power of 7
print(h)
# Equals sign for variable assignment
width = 20
height = 5 * 9
print(width * height)

# Python supports int, float, decmal and friction data types. also supports complex numbers. e.g. 3+5j where j is imaginary part
